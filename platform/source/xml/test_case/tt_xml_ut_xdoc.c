        TT_UT_EQUAL(tt_xnode_type(xn), TT_XNODE_DOCUMENT, "");
        TT_UT_EQUAL(tt_xnode_name(xn)[0], 0, "");
        // TT_UT_EQUAL(tt_xnode_value_cstr(xn)[0], 0, "");

        // invalid content
